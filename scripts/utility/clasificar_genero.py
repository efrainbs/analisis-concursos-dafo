# ── NORMALIZACIÓN ─────────────────────────────────────────────────────
def normalize(s):
    s = (s or "").strip()
    s = s.upper()
    s = unicodedata.normalize('NFD', s).encode('ascii', 'ignore').decode()
    return s

TOKENS_IGNORAR = {
    "DEL", "DE", "LA", "LAS", "LOS", "EL", "SAN", "SANTA",
    "DON", "DOÑA", "Y", "E", "A", "AL", "POR", "EN", "CON",
    "H", "V", "VAN", "VON",
}

def limpiar_nombres(raw):
    """Limpia y extrae tokens relevantes del campo nombres."""
    if not raw:
        return []
    s = normalize(raw)
    s = s.strip()
    s = s.strip('.')
    s = re.sub(r'[^A-Z\s]', ' ', s)
    tokens = [t for t in s.split() if t and len(t) >= 2 and t not in TOKENS_IGNORAR]
    return tokens


