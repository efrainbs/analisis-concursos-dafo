# ── REGLA POR TERMINACIÓN ──────────────────────────────────────────
def clasificar_por_regla(name):
    if not name or len(name) <= 2:
        return None
    if name.endswith('A'):
        return 'femenino'
    if name.endswith('O'):
        return 'masculino'
    return None


import gender_guesser.detector as gd
