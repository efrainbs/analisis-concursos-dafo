# ── PIPELINE PRINCIPAL ────────────────────────────────────────────
def _clasificar_token(token):
    """Clasifica un token individual de nombre."""
    if not token or len(token) <= 1:
        return None
    if token in KNOWN:
        return KNOWN[token]
    g = _DETECTOR.get_gender(token)
    MAP = {
        'male': 'masculino', 'female': 'femenino',
        'mostly_male': 'masculino', 'mostly_female': 'femenino',
        'andy': 'indeterminado', 'unknown': None,
    }
    result = MAP.get(g)
    if result:
        return result
    return clasificar_por_regla(token)


def clasificar(nombres_raw):
    tokens = limpiar_nombres(nombres_raw)
    if not tokens:
        return None

    # Estrategia: probar el primer token; si no resulta, probar el último
    candidatos = [tokens[0], tokens[-1]]
    visto = set()
    for tok in candidatos:
        if tok in visto:
            continue
        visto.add(tok)
        res = _clasificar_token(tok)
        if res:
            return res
    return None


